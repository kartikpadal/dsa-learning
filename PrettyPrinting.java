

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 453.1274f;
        System.out.printf("Formatted number is %.2f", a);
        // prints 453.13
        System.out.printf("Pie: %.2f", Math.PI);
        // prints 3.14
        System.out.printf("Hello my name is %s and I am %s", "Kartik", "21");
        // prints Hello my name is Kartik and I am 21
    }
}
